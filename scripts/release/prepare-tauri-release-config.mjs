import { appendFileSync, writeFileSync } from "node:fs"

const outputPath =
	process.env.TAURI_RELEASE_CONFIG_PATH ?? "apps/desktop/src-tauri/tauri.release.conf.json"
const envPath = process.env.GITHUB_ENV

function resolveUpdaterPublicKey(value) {
	const trimmed = value.trim()
	if (!trimmed) return null
	if (trimmed.includes("untrusted comment:")) return trimmed

	try {
		const decoded = Buffer.from(trimmed, "base64").toString("utf8").trim()
		if (decoded.includes("untrusted comment:")) return decoded
	} catch {}

	return trimmed
}

function encodeUpdaterPublicKey(value) {
	return Buffer.from(value, "utf8").toString("base64")
}

const publicKey = resolveUpdaterPublicKey(process.env.CORTEX_UPDATER_PUBLIC_KEY ?? "")

if (!publicKey) {
	console.error("CORTEX_UPDATER_PUBLIC_KEY is required for desktop release builds.")
	process.exit(1)
}

if (!publicKey.includes("untrusted comment:")) {
	console.error("CORTEX_UPDATER_PUBLIC_KEY must be a minisign public key.")
	console.error("Use either the decoded .pub contents or the base64 value generated by Tauri.")
	process.exit(1)
}

const configPublicKey = encodeUpdaterPublicKey(publicKey)

writeFileSync(
	outputPath,
	`${JSON.stringify({ plugins: { updater: { pubkey: configPublicKey } } }, null, "\t")}\n`,
)

if (envPath) {
	const delimiter = "CORTEX_UPDATER_PUBLIC_KEY_EOF"
	appendFileSync(
		envPath,
		`CORTEX_UPDATER_PUBLIC_KEY<<${delimiter}\n${configPublicKey}\n${delimiter}\n`,
	)
}
